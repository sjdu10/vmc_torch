"""
VMC for ab initio quantum chemistry using NNBF.

Reproducing Liu & Clark, PRB 110, 115137 (2024).

Usage:
    mpirun -np <N> python vmc_run_qc.py

Reads molecular integral data from .npz files generated by
pyscf_learn/generate_table1_data.py.
"""

import os

os.environ["OPENBLAS_NUM_THREADS"] = "1"
os.environ["MKL_NUM_THREADS"] = "1"
os.environ["OMP_NUM_THREADS"] = "1"

from mpi4py import MPI
import torch
import numpy as np

from vmc_torch.model import NNBF_QC, init_weights_to_zero
from vmc_torch.sampler import MetropolisExchangeSamplerSpinful
from vmc_torch.variational_state import Variational_State
from vmc_torch.optimizer import SGD, SR
from vmc_torch.VMC import VMC
from vmc_torch.hamiltonian_torch import MolecularHamiltonian

COMM = MPI.COMM_WORLD
SIZE = COMM.Get_size()
RANK = COMM.Get_rank()

# ============================================================
# Configuration — edit these for different molecules
# ============================================================
DATA_DIR = "/home/sijingdu/TNVMC/pyscf_learn"
MOLECULE = "H4_sto3g"  # Change this to run different molecules

# Model hyperparameters
N_DETERMINANTS = 1
HIDDEN_DIM = 128
N_HIDDEN_LAYERS = 2
NN_ETA = 1.0  # set to 0 for pure Slater determinant

# VMC hyperparameters
N_SAMPLES = int(5e3)
BURN_IN = 50
LEARNING_RATE = 1e-2
DIAG_ETA = 1e-3  # SR diagonal shift
TOTAL_STEPS = 200
INIT_STEP = 0

dtype = torch.float64

# ============================================================
# Load molecular data
# ============================================================
data = np.load(os.path.join(DATA_DIR, f"{MOLECULE}.npz"))
h1e = data["h1e"]
h2e = data["h2e"]
e_nuc = float(data["e_nuc"])
n_alpha = int(data["n_alpha"])
n_beta = int(data["n_beta"])
e_hf = float(data["e_hf"])
e_fci = float(data["e_fci"]) if "e_fci" in data else None
e_ccsd = float(data["e_ccsd"]) if "e_ccsd" in data else None
n_mo = int(data["n_mo"])

if RANK == 0:
    print(f"Molecule: {MOLECULE}")
    print(f"  Spatial MOs: {n_mo}, Spin-orbs: {2*n_mo}")
    print(f"  Electrons: {n_alpha + n_beta} "
          f"({n_alpha}a, {n_beta}b)")
    print(f"  E_nuc = {e_nuc:.8f}")
    print(f"  E_HF  = {e_hf:.8f}")
    if e_ccsd is not None:
        print(f"  E_CCSD = {e_ccsd:.8f}")
    if e_fci is not None and not np.isnan(e_fci):
        print(f"  E_FCI  = {e_fci:.8f}")
    print()

# ============================================================
# Build Hamiltonian
# ============================================================
H = MolecularHamiltonian(h1e, h2e, n_alpha, n_beta, e_nuc)
graph = H.graph

if RANK == 0:
    print(f"Hilbert space dim: {H.hilbert.size}")
    print(f"Graph edges: {graph.n_edges}")
    print()

# ============================================================
# Build model
# ============================================================
model = NNBF_QC(
    H.hilbert,
    n_determinants=N_DETERMINANTS,
    hidden_dim=HIDDEN_DIM,
    n_hidden_layers=N_HIDDEN_LAYERS,
    param_dtype=dtype,
    nn_eta=NN_ETA,
)

# Initialize first determinant to HF
model.init_hf(n_alpha, n_beta)

if RANK == 0:
    print(f"Model: NNBF_QC")
    print(f"  N_determinants: {N_DETERMINANTS}")
    print(f"  Hidden dim: {HIDDEN_DIM}")
    print(f"  N_hidden_layers: {N_HIDDEN_LAYERS}")
    print(f"  nn_eta: {NN_ETA}")
    print(f"  Total parameters: {model.num_params}")
    print()

# ============================================================
# Sampler
# ============================================================
N_SAMPLES = N_SAMPLES - N_SAMPLES % SIZE + SIZE - 1
sampler = MetropolisExchangeSamplerSpinful(
    H.hilbert,
    graph,
    N_samples=N_SAMPLES,
    burn_in_steps=BURN_IN,
    reset_chain=False,
    random_edge=True,  # random edge for complete graph
    subchain_length=n_mo,  # sweep ~ M edges per step
    equal_partition=False,
    dtype=dtype,
)

# ============================================================
# Optimizer
# ============================================================
optimizer = SGD(learning_rate=LEARNING_RATE)
preconditioner = SR(
    dense=False,
    exact=False,
    use_MPI4Solver=True,
    diag_eta=DIAG_ETA,
    rtol=1e-5,
    iter_step=1000,
    dtype=dtype,
)

# ============================================================
# Variational state & VMC
# ============================================================
variational_state = Variational_State(
    model, hi=H.hilbert, sampler=sampler, dtype=dtype
)
vmc = VMC(
    hamiltonian=H,
    variational_state=variational_state,
    optimizer=optimizer,
    preconditioner=preconditioner,
)

# ============================================================
# Run
# ============================================================
if __name__ == "__main__":
    out_dir = (
        f"./qc_data/{MOLECULE}/"
        f"D{N_DETERMINANTS}_h{HIDDEN_DIM}_eta{NN_ETA}"
    )
    os.makedirs(out_dir, exist_ok=True)

    if RANK == 0:
        # Save run config
        config = {
            "molecule": MOLECULE,
            "n_mo": n_mo,
            "n_alpha": n_alpha,
            "n_beta": n_beta,
            "e_nuc": e_nuc,
            "e_hf": e_hf,
            "e_fci": e_fci,
            "e_ccsd": e_ccsd,
            "n_determinants": N_DETERMINANTS,
            "hidden_dim": HIDDEN_DIM,
            "n_hidden_layers": N_HIDDEN_LAYERS,
            "nn_eta": NN_ETA,
            "n_samples": N_SAMPLES,
            "learning_rate": LEARNING_RATE,
            "diag_eta": DIAG_ETA,
        }
        np.savez(os.path.join(out_dir, "config.npz"), **config)

    vmc.run(
        INIT_STEP,
        INIT_STEP + TOTAL_STEPS,
        tmpdir=out_dir,
        save_every=50,
    )
